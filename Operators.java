package basic;
//Operators are the symbol that perform operation on values.
public class Operators {
    public static void main(String[] args) {
/*
        Arithmetic Operator + - * / %
        Operation perform b/w two operands

        System.out.println(2+3);
        System.out.println(2-3);
        System.out.println((float) 2/3);
        System.out.println((float) 2%3); // It gives remainder
        System.out.println(2*3);
*/

/*
        Unary Operators ++2 2++ --1 1--
        int a = 1;
        System.out.println(++a); //Increment then print
        System.out.println(--a); //Decrement then print
        System.out.println(a--); //Print then Decrement
        System.out.println(a++); //Print then Increment
        System.out.println(++a - --a);
*/

/*
        Assignment Operator = += -= *= %= /=
        int b = 3;
        System.out.println(b+=2);
        System.out.println(b-=2);
        System.out.println(b*=2);
        System.out.println(b/=2);
        System.out.println(b%=2);
*/

/*
        Comparison Operator == <= >= > < !=
        int c = 5;
        if(c==5){
            System.out.println("Equal");
        }
        if(c<10){
            System.out.println("Smaller then 10");
        }
        if(c>0){
            System.out.println("Larger then 0");
        }

*/


/*
      Logical Operator &&  || !
        boolean a = true;
        boolean b = true;
        if(a && b){
            System.out.println("true");
        }
        if(!a || b){
            System.out.println("False");
        }
        if(a || b){
            System.out.println("True");
        }
//        We can also write like this
        System.out.println(13<14 && 9>10);

*/


/*
      Bitwise Operator & |
        boolean a = true;
        boolean b = true;
        if(a & b){
            System.out.println("true");
        }
        if(a | b){
            System.out.println("True");
        }
*/

/*
        Difference b/w && and & operator
        && -> If first condition is false do not check second
        &  -> Check both condition whether First is false
        || -> If first condition is true do not check second
        |  -> Check both condition whether First is true
*/


    }
}
