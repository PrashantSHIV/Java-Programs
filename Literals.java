package basic;
//Literals are the constant value that assign to variable

public class Literals {
    public static void main(String[] args) {
        int a = 4;
        long l = 12345678890L; // L are used to tell it is long value      BY DEFAULT INT
        float f = 123.F;       // F are used to tell it is float value     BY DEFAULT DOUBLE
        short s = 32767;
        boolean wrong = false;
        String name = "Java"; // support string type
        System.out.println(name);
    }
}
